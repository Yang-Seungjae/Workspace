package kr.ac.kopo.day06.exam;

public class LoopMainPrac16 {

	
	public static void main(String[] args) {
		
		
		int star = 9;
		for(int i = 1; i <=5; i++) {
			
			for(int j = 1; j<= i - 1; j++) {
				
				System.out.print(" ");
			}
			for(int j = 1; j <= star; j++) {
				
				System.out.print("*");
				
			}
			System.out.println();
			star-=2;
			
		}
            

	}
            
	}
	


		



			
		

			
		
	
	
	






	

